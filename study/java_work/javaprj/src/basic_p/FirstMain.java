package basic_p; // 패키지 지정 - 가장 상위에 존재

public class FirstMain {   //클래스 시작 - public : 접근제한자
	
	
	// 메소드 정의
	/* public : 접근제한자
	 	static :  정적요소
	 	void : 리턴형이 없다
	 	main : 메소드 명 - 시작하는 메소드는 main 으로 고정
	 	String [] args : 매개변수 
	 	String [] : 자료형
	 */
	public static void main(String[] args) {  //main 메소드 시작
		
		// 터미널 출력 메소드
		System.out.println("안녕하세요");
		
	}		// main 메소드 끝

}			//클래스 끝
